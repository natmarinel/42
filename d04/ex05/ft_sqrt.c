int	ft_sqrt(int nb)
{
	int	x;
	int y;

	y = 0;
	x = y * y;
	while (x != nb && x <= nb)
	{
		x = y * y;
		y++;
	}
	if (nb <= 0)
		return (0);
	if (x != nb)
		return (0);
	else
		return (y - 1);
}
